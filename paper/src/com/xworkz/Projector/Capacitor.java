package com.xworkz.Projector;

public class Capacitor {
    public String capacitorType;
    public int capacitorPrice;

    public String getCapacitorType()
    {
        return this.capacitorType;
    }
    public void setCapacitorType(String capacitorType)
    {
        this.capacitorType=capacitorType;
    }
    public int getCapacitorPrice()
    {
        return this.capacitorPrice;
    }
    public void setCapacitorPrice(int capacitorPrice){
        this.capacitorPrice=capacitorPrice;
    }
}
